#!/bin/bash
set -euo pipefail
# Bunker Pulse: triage bug memos from wiki/inbox.
# Default mode is dry-run. Use --apply to create branches/issues/PRs/archive files.

APPLY=0
if [[ "${1:-}" == "--apply" ]]; then
  APPLY=1
  shift
fi

BUNKER_HOME="${BUNKER_HOME:-$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)}"
INBOX_DIR="${BUNKER_INBOX_DIR:-$BUNKER_HOME/wiki/inbox}"
ARCHIVE_DIR="${BUNKER_ARCHIVE_DIR:-$BUNKER_HOME/wiki/archive}"
REPO="${BUNKER_GITHUB_REPO:-SamBleed/opencode-obsidian}"

cd "$BUNKER_HOME"
mkdir -p "$ARCHIVE_DIR"

echo "🏛️ Bunker Pulse active. Mode: $([[ "$APPLY" -eq 1 ]] && echo APPLY || echo DRY-RUN)"

shopt -s nullglob
for file in "$INBOX_DIR"/*.md; do
  filename=$(basename "$file")
  if ! grep -qE "type: bug|tags: \[bug\]" "$file"; then
    continue
  fi

  title=$(grep -v "^---" "$file" | grep -vE "^type:|^created:|^source:|^tags:" | grep -v "^$" | head -n 1 | sed 's/^[ \t]*//')
  title="${title:-$filename}"
  user_report=$(awk '/---/{count++} count==2 {if (!/---/) print}' "$file")

  culprit_file="unknown"
  for word in $(echo "$title" | sed 's/[^a-zA-Z0-9.\/_ -]//g'); do
    if [[ -f "$word" ]]; then culprit_file="$word"; break; fi
    if [[ -f "bin/$word" ]]; then culprit_file="bin/$word"; break; fi
  done

  diagnostic="## Forensic Analysis\n"
  if [[ "$culprit_file" != "unknown" ]]; then
    context=$(grep -nE "for |while |find |\\*\\.md" "$culprit_file" | head -n 5 || true)
    diagnostic+="File: \`$culprit_file\`\n\nSuspicious context:\n\`\`\`bash\n$context\n\`\`\`\n"
    root_cause="The system may be constrained by rigid source or scan patterns in \`$culprit_file\`."
  else
    diagnostic+="No automatic file mapping found. Manual triage required.\n"
    root_cause="Manual triage required to identify the affected component."
  fi

  report_body="## Description\n$user_report\n\n## Root Cause\n$root_cause\n\n$diagnostic\n\n## Proposed Fix\n1. Review the affected component.\n2. Add a regression note or test.\n3. Run \`bin/wiki-integrity.sh\`.\n\n---\nGenerated by Bunker Pulse."
  branch_name="fix/autopilot-$(date +%s)"

  echo "Bug memo: $title"
  if [[ "$APPLY" -ne 1 ]]; then
    echo "DRY RUN: would create branch $branch_name"
    echo "DRY RUN: would create GitHub issue in $REPO"
    echo "DRY RUN: would create draft PR"
    echo "DRY RUN: would archive $file -> $ARCHIVE_DIR/$filename"
    continue
  fi

  command -v gh >/dev/null || { echo "gh CLI is required for --apply" >&2; exit 1; }
  git checkout -b "$branch_name"
  git commit --allow-empty -m "chore: forensic triage for $title"
  git push origin "$branch_name"
  issue_url=$(gh issue create --repo "$REPO" --title "fix: $title" --body "$(echo -e "$report_body")" --label "bug")
  gh pr create --repo "$REPO" --title "fix: $title (Forensic Draft)" --body "Closing $issue_url automatically after review." --draft --base main --head "$branch_name"
  git checkout main
  mv "$file" "$ARCHIVE_DIR/$filename"
done

echo "🏁 Bunker Pulse completed."
