#!/bin/bash
set -euo pipefail

# Bunker Pulse v5.2.1: The Infallible Architect
INBOX_DIR="/home/sam/opencode-obsidian/wiki/inbox"
ARCHIVE_DIR="/home/sam/opencode-obsidian/wiki/archive"
REPO="SamBleed/opencode-obsidian"

mkdir -p "$ARCHIVE_DIR"

echo "🏛️ Bunker Pulse v5.2.1 [FIXED] activo..."

for file in "$INBOX_DIR"/*.md; do
    [ -e "$file" ] || continue
    filename=$(basename "$file")
    
    if grep -q "type: bug\|tags: \[bug\]" "$file"; then
        # 1. EXTRACCIÓN LIMPIA (Título y Cuerpo)
        title=$(grep -v "^---" "$file" | grep -v "^type:\|^created:\|^source:\|^tags:" | grep -v "^$" | head -n 1 | sed 's/^[ \t]*//')
        # Capturamos todo lo que está después del frontmatter
        user_report=$(awk '/---/{count++} count==2 {if (!/---/) print}' "$file")
        
        echo "🔍 Analizando: $title"
        
        # 2. LOCALIZACIÓN CON FILTRO CORREGIDO
        culprit_file="unknown"
        for word in $(echo "$title" | sed 's/[^a-zA-Z0-9.\/_ \-]//g'); do
            if [ -f "$word" ] || [ -f "bin/$word" ]; then
                [ -f "$word" ] && culprit_file="$word" || culprit_file="bin/$word"
                break
            fi
        done

        # 3. CONSTRUCCIÓN DEL INFORME
        diagnostic="## 📑 Análisis Forense\n"
        if [ "$culprit_file" != "unknown" ]; then
            diagnostic+="📍 **Archivo:** \`$culprit_file\`\n"
            # Buscamos patrones de 'scan' o 'loop' si es un bug de escaneo
            context=$(grep -nE "for |while |find |\*\.md" "$culprit_file" | head -n 5)
            diagnostic+="📝 **Código Sospechoso:**\n\`\`\`bash\n$context\n\`\`\`\n"
            root_cause="El sistema parece estar limitado por patrones de búsqueda rígidos (ej: \`*.md\`) definidos en el código fuente."
        else
            diagnostic+="📍 No se pudo mapear automáticamente a un archivo del bin/.\n"
            root_cause="Se requiere triaje manual para identificar el componente afectado."
        fi

        report_body="## Description\n$user_report\n\n"
        report_body+="## Root Cause\n$root_cause\n\n$diagnostic\n\n"
        report_body+="## Proposed Fix\n"
        report_body+="1. Expand the file pattern to support other formats (e.g. \`*.{md,txt,json}\`).\n"
        report_body+="2. Update the scanning logic in \`$culprit_file\`.\n\n"
        report_body+="---\n*Generated by Bunker Architect v5.2.1*"

        # 4. GITHUB
        branch_name="fix/autopilot-$(date +%s)"
        git checkout -b "$branch_name"
        git commit --allow-empty -m "chore: forensic triage for $title"
        git push origin "$branch_name"
        
        echo "🐞 Enviando reporte de alta fidelidad..."
        issue_url=$(gh issue create --title "fix: $title" --body "$(echo -e "$report_body")" --label "bug")
        gh pr create --title "fix: $title (Forensic Draft)" --body "Closing $issue_url automatically after review." --draft --base main --head "$branch_name"
        
        git checkout main
        mv "$file" "$ARCHIVE_DIR/$filename"
    fi
done
echo "🏁 Ciclo completado."
